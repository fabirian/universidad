    @Override
    public void update(Observable obs, Object arg) {
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("Profesi√≥n: " + cliDB.getProfesion());
        graficar(cliDB.getTotalHombres(), cliDB.getTotalMujeres());

    }
    private void graficar(int hombres, int mujeres){
 
        int total=hombres+mujeres;
        pbHombres.setValue((hombres*100)/total);
        pbMujeres.setValue((mujeres * 100)/total);
        lblPorHombre.setText((Integer.toString((hombres*100)/total))+"%");
        lblPorMujer.setText((Integer.toString((mujeres*100)/total))+"%");
    }
